  <h2>DOCUMENTARY REQUIREMENTS<br>FOR COMMON GOVERNMENT TRANSACTIONS</h2>

  <h4>Fundamental Principles governing the financial transactions and operations of any government agency as provided under Section 4 of Presidential Decree (PD) No. 1445:</h4>

  <ol>
    <li>No money shall be paid out of any public treasury or depository except in pursuance of an appropriation law or other specific statutory authority.</li>
    <li>Government funds or property shall be spent or used solely for public purposes.</li>
    <li>Trust funds shall be available and may be spent only for the specific purpose for which the trust was created or the funds received.</li>
    <li>Fiscal responsibility shall, to the greatest extent, be shared by all those exercising authority over the financial affairs, transactions, and operations of the government agency.</li>
    <li>Disbursements or disposition of government funds or property shall invariably bear the approval of the proper officials.</li>
    <li>Claims against government funds shall be supported with complete documentation.</li>
    <li>All laws and regulations applicable to financial transactions shall be faithfully adhered to.</li>
    <li>Generally accepted accounting principles and practices as well as sound management and fiscal administration shall be observed, provided that they do not contravene existing laws and regulations.</li>
  </ol>

  <h4>General Requirements for All Types of Disbursement</h4>
  <ol>
    <li>Certificate of Availability of Funds issued by the Chief Accountant</li>
    <li>Existence of lawful and sufficient allotment duly obligated as certified by authorized officials (except for government-owned and controlled corporations/government financing institutions)</li>
    <li>Legality of transaction and conformity with laws, rules, or regulations</li>
    <li>Approval of expenditure by Head of Office or his authorized representative</li>
    <li>Sufficient and relevant documents to establish validity of claim</li>
  </ol>

  <h4>Specific Requirements for Each Type of Disbursement</h4>

  <h4>1.0 Cash Advances</h4>
  <h4>1.1 Granting of Cash Advances</h4>

  <p><em>General Guidelines</em></p>

  <p>
    The rules and regulations on the grant and liquidation of cash advances are prescribed under COA Circular No. 97-002 dated February 10, 1997 and reiterated under COA Circular No. 2009-002 dated May 18, 2009 and Section 89 of PD No. 1445. These guidelines provide, among others, that:
  </p>

  <ul>
    <li>No cash advance shall be given unless for a legally authorized specific purpose.</li>
    <li>No additional cash advances shall be allowed to any official or employee unless the previous cash advance given to him is first liquidated and accounted for.</li>
    <li>Cash advances shall be granted for payments on account of infrastructure projects or other undertaking on a project basis.</li>
    <li>A cash advance shall be reported as soon as the purpose for which it was given has been served.</li>
  </ul>

